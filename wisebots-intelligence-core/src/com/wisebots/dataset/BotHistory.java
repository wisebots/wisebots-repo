package com.wisebots.dataset;

public class BotHistory {

	private String gameName;
	private String id;
	private String nustates;
	private String method;
	private String epochs;	
	private String time;
	private String quality;
	private String timerefresh;
	private String victory;
	private String draw;
	
	public String getGameName() {
		return gameName;
	}
	public void setGameName(String gameName) {
		this.gameName = gameName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getEpochs() {
		return epochs;
	}
	public void setEpochs(String epochs) {
		this.epochs = epochs;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getQuality() {
		return quality;
	}
	public void setQuality(String quality) {
		this.quality = quality;
	}
	public String getTimerefresh() {
		return timerefresh;
	}
	public void setTimerefresh(String timerefresh) {
		this.timerefresh = timerefresh;
	}
	public String getVictory() {
		return victory;
	}
	public void setVictory(String victory) {
		this.victory = victory;
	}
	public String getDraw() {
		return draw;
	}
	public void setDraw(String draw) {
		this.draw = draw;
	}
	public String getNustates() {
		return nustates;
	}
	public void setNustates(String nustates) {
		this.nustates = nustates;
	}
	
	
}
